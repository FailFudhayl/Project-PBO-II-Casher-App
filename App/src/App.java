    private void beliBUTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliBUTActionPerformed
        try {
            if(jumlahTF.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Masukkan Jumlah Barang yang Dibeli Terlebih Dahulu");
            } else {
                Produk produk = ControllerDB.getProdukbyNama(produkTF.getText());
                int pengurangan =Integer.parseInt(jumlahTF.getText());
                int newStok = (produk.getStock())-pengurangan;
                if (newStok>=1){
                    ControllerDB.updateStokDB(produkTF.getText(), newStok);
                    updateTableProduk();
                }else{
                    ControllerDB.deletDB(selectName());
                    updateTableProduk();
                }  
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pilih produk sebelum mengedit!/Masukkan Jumlah Produk yang ingin Dibeli");
        }
        
    }//GEN-LAST:event_beliBUTActionPerformed

