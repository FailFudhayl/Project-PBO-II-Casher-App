    private void tambahBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBTActionPerformed
        if (namaTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama barang belum di input");
        } else if (hargaTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Harga barang belum di input");
        } else if(stokTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Stok barang belum di input");
        } else {
            ControllerDB.insertDB(namaTF.getText(), Long.valueOf(hargaTF.getText()), Integer.valueOf(stokTF.getText()));
            JOptionPane.showMessageDialog(null, "Barang berhasil diinput, refresh tabel!");
            updateTableProduk();
            clear();
        }
    }//GEN-LAST:event_tambahBTActionPerformed

