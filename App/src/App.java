    private void editBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBTActionPerformed
          try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rowSelect = jTable1.getSelectedRow();

            if ("Edit".equals(editBT.getText())) {
                tambahBT.setEnabled(false);
                deleteBT.setEnabled(false);
                editBT.setText("Update");
                namaTF.setText(model.getValueAt(rowSelect, 1).toString());
                hargaTF.setText(model.getValueAt(rowSelect, 2).toString());
                stokTF.setText(model.getValueAt(rowSelect, 3).toString());
                namaTF.setEditable(false);
            } else {
                namaTF.setEditable(true);
                tambahBT.setEnabled(true);
                deleteBT.setEnabled(true);
                editBT.setText("Edit");
                ControllerDB.updateProdukDB(namaTF.getText(), Long.valueOf(hargaTF.getText()), Integer.parseInt(stokTF.getText()));
                updateTableProduk();
                clear();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pilih produk sebelum mengedit!");
        }
    }//GEN-LAST:event_editBTActionPerformed

