    private void updateTotalHarga(String produk, String harga) {
        try {
            produkTF.setText(produk);
            Long longHarga = Long.valueOf(harga);
            int intJumlah = Integer.parseInt(jumlahTF.getText());
            totalTF.setText(String.format("Rp. %d", longHarga * intJumlah));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void updateTableProduk() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Produk i : ControllerDB.getDBProduk()){
            insertDataProduk(i.getName(), String.valueOf(i.getPrice()), String.valueOf(i.getStock()));
        }
    }
    
    public void updateTableRiwayat() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for (Riwayat i : ControllerDB.getDBPRiwayat()){
            insertDataRiwayat(i.getTimestamp(), i.getProduk(), String.valueOf(i.getJumlah()), String.valueOf(i.getHarga_dibeli()), String.valueOf(i.getUang_tunai()), String.valueOf(i.getUang_kembali()));
        }
    }
    
    public String selectName(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rowSelected = jTable1.getSelectedRow();
        
        String valueNama = (String) model.getValueAt(rowSelected, 1);
        return valueNama;
    }
            
